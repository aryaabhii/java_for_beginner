// Program based on Looping Concept...

class Loop_3 {
    public static void main(String args[]) {
        System.out.println("Program to print the sum of 10 natural number.\n");
        int sum = 0;
        for (int i = 1; i < 11; i++) {
            sum = sum + i;
        }
        System.out.println(sum);
    }
}